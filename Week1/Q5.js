// Solve Increment & Decrement Operator Questions

let a = 10;
let b = 20;
let c = 30;

console.log(a++); // 10
console.log(++a); // 12
console.log(a);   // 12

b++;              // 21
console.log(b);   // 21

++c;              // 31
console.log(c);   // 31

console.log(++b + c++); // 22 + 31 = 53

console.log(c);   // 32